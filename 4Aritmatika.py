bilangan1 = int(input("masukkan bilangan 1 : "))
bilangan2 = int(input("masukkan bilangan 2 : "))
hasil = bilangan1 + bilangan2
print(f"{bilangan1} + {bilangan2} = {hasil}")

# Exponential (perpangkatan)
print(f"4 pangakat 3 = {4**3}")

# Modulus
print(f"modulus dari 44 % 31 = {44%31}")

# Division
print(f"44 / 31 = {44/31}")

# Floor Division
angka1 = 17
angka2 = 6
hasil2 = angka1 // angka2
print(f"floor division dari {angka1} // {angka2} = {hasil2}")